    public void pesquisar(){
        String sql= "select * from tb_usuarios where id = ?";
        try{
            pst= conexao.prepareStatement(sql);
            pst.setString(1, txtUsu.getText());
            rs= pst.executeQuery();
            
            if(rs.next()){
                txtNome.setText(rs.getString(1));
                txtLog.setText(rs.getString(2));
                txtPass.setText(rs.getString(3));
            } else{
                txtNome.setText(null);
                txtLog.setText(null);
                txtPass.setText(null);
                JOptionPane.showMessageDialog(null, "n√£o cadastrado");
                
            }
            
       }catch (Exception e){
           JOptionPane.showMessageDialog(null, "metodo pesquisar"+e);
       }
        
    }
    
    public void limpar(){
        txtNome.setText(null);
        txtLog.setText(null);
        txtPass.setText(null);
        
    }

